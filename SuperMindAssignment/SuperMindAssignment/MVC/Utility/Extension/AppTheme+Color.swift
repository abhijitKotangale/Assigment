import Foundation
import UIKit

//MARK: App Theme Color
extension UIColor {
    struct AppTheme {
        static var primaryColor: UIColor {
            return UIColor(named: "PrimaryColor")!
        }
        static var textColor: UIColor {
            return UIColor(named: "TextColor")!
        }
    }
}

